package io.github.railroad.core.vcs;

/**
 * An enumeration representing the types of repositories supported by the system.
 * This enum is used to distinguish between different version control systems.
 */
public enum RepositoryTypes {
    /**
     * Represents a Git repository.
     */
    GIT,

    /**
     * Represents an SVN (Subversion) repository.
     */
    SVN
}